
// this class is creating for purpose understanding module in node js.

class Arithmetic{
    add(a,b){
        return a+b;
    }
    sub(a,b){
        return a-b;
    }
    mul(a,b){
        return a*b;
    }
    div(a,b){
        return a/b;
    }
}

// here we export the class Arithmetic
module.exports = Arithmetic;